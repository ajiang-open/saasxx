package com.saasxx.core.module.common.vo;

public class VAddress {
	String countryCode;
	String countryName;
	String provinceCode;
	String provinceName;
	String cityCode;
	String cityName;
	String regionCode;
	String regionName;
	String address;

	public String getCountryCode() {
		return countryCode;
	}

	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}

	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}

	public String getProvinceCode() {
		return provinceCode;
	}

	public void setProvinceCode(String provinceCode) {
		this.provinceCode = provinceCode;
	}

	public String getProvinceName() {
		return provinceName;
	}

	public void setProvinceName(String provinceName) {
		this.provinceName = provinceName;
	}

	public String getCityCode() {
		return cityCode;
	}

	public void setCityCode(String cityCode) {
		this.cityCode = cityCode;
	}

	public String getCityName() {
		return cityName;
	}

	public void setCityName(String cityName) {
		this.cityName = cityName;
	}

	public String getRegionCode() {
		return regionCode;
	}

	public void setRegionCode(String regionCode) {
		this.regionCode = regionCode;
	}

	public String getRegionName() {
		return regionName;
	}

	public void setRegionName(String regionName) {
		this.regionName = regionName;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	@Override
	public String toString() {
		return "VAddress [countryCode=" + countryCode + ", countryName=" + countryName + ", provinceCode="
				+ provinceCode + ", provinceName=" + provinceName + ", cityCode=" + cityCode + ", cityName=" + cityName
				+ ", regionCode=" + regionCode + ", regionName=" + regionName + ", address=" + address + "]";
	}

}
